import torch
import torch.nn as nn
import torch.nn.functional as F
from torch.utils.data import DataLoader
import torchvision
import torchvision.datasets as dsets
import matplotlib.pyplot as plt

torch.manual_seed(1)  # Reproducibility


class SoftmaxClassifierModel(nn.Module):
    def __init__(self):
        super().__init__()
        self.linear = nn.Linear(28 * 28, 10, bias=True)

    def forward(self, x):
        return self.linear(x)


mnist_train = dsets.MNIST(root="MNIST_data/", train=True,
                          transform=torchvision.transforms.ToTensor(), download=True)
mnist_test = dsets.MNIST(root="MNIST_data/", train=False,
                         transform=torchvision.transforms.ToTensor(), download=True)

train_loader = DataLoader(mnist_train,
                          batch_size=100,
                          shuffle=True,
                          drop_last=True)
test_loader = DataLoader(mnist_test)


model = SoftmaxClassifierModel()
optimizer = torch.optim.SGD(model.parameters(), lr=1e-1)

training_loss = []
test_loss = []


nb_epoch = 15
for epoch in range(1, nb_epoch + 1):
    print("epoch:\t{}".format(epoch))

    # Training
    temp = []

    for image, label in train_loader:
        # for thing in image:
        #     plt.imshow(thing.squeeze(), cmap="Greys", interpolation="nearest")
        #     plt.show(block=False)
        #     plt.pause(1)
        #     plt.close()

        image = image.view(-1, 28 * 28)

        prediction = model(image)

        cost = F.cross_entropy(prediction, label)
        temp.append(cost.item())

        optimizer.zero_grad()
        cost.backward()
        optimizer.step()

    training_loss.append(sum(temp) / len(temp))

    # Test
    temp = []

    model.eval()
    with torch.no_grad():
        for image, label in test_loader:
            image = image.view(-1, 28 * 28)

            prediction = model(image)

            cost = F.cross_entropy(prediction, label)
            temp.append(cost.item())

    test_loss.append(sum(temp) / len(temp))


fig = plt.figure()
result = fig.add_subplot()

result.plot(training_loss, label='Training')
result.plot(test_loss, label='Test')

result.legend()
plt.title("Loss")
plt.show()


