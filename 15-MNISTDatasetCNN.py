import torch
import torch.nn as nn
from torch.utils.data import DataLoader
import torchvision
import matplotlib.pyplot as plt


device = "cuda" if torch.cuda.is_available() else "cpu"
torch.manual_seed(1)  # Reproducibility
if device == "cuda":
    torch.cuda.manual_seed(1)
print("device =", device)


class CNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.layer1 = nn.Sequential(
            nn.Conv2d(1, 32, kernel_size=3, stride=1, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2)
        )
        self.layer2 = nn.Sequential(
            nn.Conv2d(32, 64, kernel_size=3, stride=1, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2)
        )
        self.fc = nn.Linear(7 * 7 * 64, 10, bias=True)

        nn.init.xavier_uniform_(self.fc.weight)

    def forward(self, x):
        out = self.layer1(x)
        out = self.layer2(out)
        out = out.view(out.size(0), -1)
        out = self.fc(out)

        return out


mnist_train = torchvision.datasets.MNIST(root="MNIST_data/", train=True,
                                         transform=torchvision.transforms.ToTensor(), download=True)
mnist_test = torchvision.datasets.MNIST(root="MNIST_data/", train=False,
                                        transform=torchvision.transforms.ToTensor(), download=True)

train_loader = DataLoader(dataset=mnist_train,
                          batch_size=100,
                          shuffle=True,
                          drop_last=True)
test_loader = DataLoader(dataset=mnist_test,
                         batch_size=len(mnist_test))


model = CNN().to(device)
criterion = nn.CrossEntropyLoss().to(device)
optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)

training_loss = []


nb_epoch = 15
for epoch in range(1, nb_epoch + 1):
    print("epoch: {}".format(epoch), end='\t| ')

    # Training
    model.train()
    for image, label in train_loader:
        image = image.to(device)
        label = label.to(device)

        prediction = model(image)

        cost = criterion(prediction, label)
        training_loss.append(cost.item())

        optimizer.zero_grad()
        cost.backward()
        optimizer.step()

    # Test
    with torch.no_grad():
        model.eval()
        for image, label in test_loader:
            image = image.to(device)
            label = label.to(device)

            prediction = model(image)

            correct_prediction = torch.argmax(prediction, 1) == label
            accuracy = correct_prediction.float().mean()
            print("accuracy: {:.5f}".format(accuracy.item()))


